      if !Globals.use_set then
	    let rs1, prf1 = heap_entail_conjunct_lhs prog is_folding is_universal ctx_L f1 pos in
	    let rs2, prf2 = heap_entail_conjunct_lhs prog is_folding is_universal ctx_R f2 pos in
