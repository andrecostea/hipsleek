	      else if (CP.mem fr struc_expl_inst) then
	        let tmp = CP.mkEqVar fr t pos in
		    if br_label = "" then
		      let res = CP.mkAnd tmp rest_right_eqns pos in
		      ((rest_left_eqns, rest_left_eqns_br),(res, rest_right_eqns_br),s_list)
