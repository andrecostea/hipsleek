	h_formula_view_label = v_lbl;
	h_formula_view_arguments = vs}) ->(*!!Attention: there might be several nodes pointed to by the same pointer as long as they are empty*)
      if CP.mem p aset then
	    match prog with
	      | Prog prog ->
